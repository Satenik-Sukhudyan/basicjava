package basicjavaprogram;

public class WhileLoop {

	public static void main(String[] args) {
		
		int i = 1; //declaration and initialization - starting value =1
		while (i<=10) //while loop runs while <=10
		{
			
			System.out.println(i);
			i++; //increases i by 1
		}

	}

}
